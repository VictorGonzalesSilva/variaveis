/* Este é um script para cálculo de idade!
Declara o ano atual para fazer o cálculo */
var anoAtual = 2021

// Pede que o usuário digite o ano em que nasceu
var anoNascimento = prompt ('Digite o ano em que nasceu');

//Calcula a idade do usuário e armazena na viriável
var idade = anoAtual - anoNascimento

// Mostra ao usuario a idade qeu ele tem
alert ('Sua idade é:'  + idade + ' anos');